def vowelCount(s):
    count=0
    count+=s.count("a")
    count+=s.count("e")
    count+=s.count("i")
    count+=s.count("o")
    count+=s.count("u")
    count+=s.count("A")
    count+=s.count("E")
    count+=s.count("I")
    count+=s.count("O")
    count+=s.count("U")
    return count

def areAnagrams(s1, s2):
    s3=s1.lower()
    s4=s2.lower()
    if(s1==s2):
        return True
    for c in string.ascii_letters:
        if(s3.count(c)!=s4.count(c)):
            return False
    return True

def patternedMessage(message, pattern):
    result=""
    message=message.replace(" ","")
    counter=0
    for c in pattern:
        if(c in string.whitespace):
            result+=c
            counter+=1
        else:
            n=(len(result)-counter)%len(message)
            result+=message[n]
    return result

